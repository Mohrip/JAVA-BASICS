package InDepth.Section6;

public class TraditionalSwitch {
    public static void main(String[] args){


char character = 'A';

switch(character){
    case 'A':
        System.out.println("A is able");
        break;
    case 'B':
        System.out.println("B is best");
        break;
    case 'C':
        System.out.println("C is cat");
        break;

        case 'D':
        System.out.println("D is dog");
        break;

        case 'E':
        System.out.println("E is elephant");


    default:
        System.out.println("Invalid character");

}        
        
    }
    
}
